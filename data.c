U64 generate_bishop_occupancy(int square){
    U64 temp = 0;
    int i = square / 8;
    int j = square & 0x7;
    int rank = i;
    int file = j;
    while(rank < 6 && file < 6){
        m_set_bit(temp, (++rank)*8+(++file));
    }
    rank = i;
    file = j;
    while(rank > 1 && file < 6){
        m_set_bit(temp, (--rank)*8+(++file));
    } 
    rank = i;
    file = j;
    while(rank > 1 && file > 1){
        m_set_bit(temp, (--rank)*8+(--file));
    }
    rank = i;
    file = j;
    while(rank < 6 && file > 1){
        m_set_bit(temp, (++rank)*8+(--file));
    } 
    return temp;
}
